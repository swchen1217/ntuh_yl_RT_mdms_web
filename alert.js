function HideAlert() {
    $('#alert-primary').hide();
    $('#alert-success').hide();
    $('#alert-danger').hide();
    $('#alert-warning').hide();
    $('#alert-info').hide();

    $('#alert-success-in').text('');
    $('#alert-danger-in').text('');
    $('#alert-warning-in').text('');
    $('#alert-info-in').text('');
}